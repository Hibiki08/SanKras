        }

        $pager = new Pagination(['totalCount' => $blog->count(), 'pageSize' => Blog::ART_SIZE]);
        $pager->pageSizeParam = false;

        $blog = $blog->offset($pager->offset)
            ->limit($pager->limit)
            ->all();

        return $this->render('articles', [
            'articles' => $blog,
            'pager' => $pager,
        ]);
    }

